  CREATE OR REPLACE FUNCTION "DAILY"."CHECK_RESERVE_FUNC" (
 f_checkin IN VARCHAR2,
 f_checkout IN VARCHAR2,
  f_roomNo IN VARCHAR2
) RETURN NUMBER IS

room_cnt NUMBER := 0;
quantity NUMBER := 0;

BEGIN
  SELECT COUNT(*) INTO room_cnt
  FROM RESERVATION
  WHERE ROOM_NO = f_roomNo
  AND f_checkin < TO_CHAR(checkout,'YYYY-MM-DD') AND f_checkout >= TO_CHAR(checkin,'YYYY-MM-DD');
    
  SELECT ROOM_QUANTITY INTO quantity
  FROM ROOM
  WHERE ROOM_NO = f_roomNO;
  
  if room_cnt >= quantity then 
    RETURN 0;
  END IF;
  
  RETURN 1;
  
END check_reserve_func;

