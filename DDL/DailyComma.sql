  CREATE OR REPLACE PROCEDURE "DAILY"."RESERVATION_CANCEL_PROC" (
  p_reserve_no IN RESERVATION.RESERVE_NO%TYPE,
  p_mypoint_cursor OUT SYS_REFCURSOR
) IS

v_member_no MEMBER.MEMBER_NO%TYPE;
v_deduction_point RESERVATION.DEDUCTION_POINT%TYPE;

BEGIN      
      
    SELECT MEMBER_NO, DEDUCTION_POINT 
    INTO v_member_no, v_deduction_point
    FROM RESERVATION
    WHERE RESERVE_NO = p_reserve_no;
    
    UPDATE RESERVATION
    SET RESERVE_STATE = 'D3'
    WHERE RESERVE_NO = p_reserve_no;
    
    UPDATE MEMBER
    SET MEMBER_POINT = MEMBER_POINT + v_deduction_point
    WHERE MEMBER_NO = v_member_no;    
    commit; 
    
    BEGIN
    OPEN p_mypoint_cursor
    FOR 
     		SELECT R.RESERVE_NO, R.RESERVE_DATE, L.COMPANY, M.ROOM_NAME,
               EXTRACT( DAY FROM R.CHECKOUT-R.CHECKIN) AS CHECKDATE, 
               R.RESERVE_PRICE, R.RESERVE_STATE
        FROM RESERVATION R JOIN LODGMENT L
        ON R.LODGMENT_NO = L.LODGMENT_NO
        JOIN ROOM M
        ON M.ROOM_NO = R.ROOM_NO
        JOIN MEMBER F
        ON F.MEMBER_NO = R.MEMBER_NO
        WHERE F.MEMBER_NO = v_member_no
        ORDER BY R.RESERVE_DATE DESC;
      END;
    
EXCEPTION
WHEN OTHERS THEN
  rollback;    
END RESERVATION_CANCEL_PROC;

